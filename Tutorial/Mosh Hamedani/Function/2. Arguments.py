# Python closer to C / C++ naming convention.
def greet(first_name, second_name):  # (first_name, second_name): Parameters of function.
    print(f"Hello {first_name}, welcome {second_name}\n")  # f"": formatted strings.


greet("Sofiullah", "Kiron")  # ("Sofiullah", "Kiron"): Arguments that are passed.
