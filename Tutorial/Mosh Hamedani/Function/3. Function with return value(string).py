def get_greeting(name):  # Greet: অভিবাদন জানানো।
    return f"Hey {name}"


message = get_greeting("Kiron")  # Here is a dynamically typed variable named: message.
print(print(message))  # "None" is a by default return value of any void function.

message = 3
print(message)  # Dynamically changed the value of the variable.
